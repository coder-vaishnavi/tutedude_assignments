import math

# Ask user for input
num = float(input("Enter a number: "))

# Perform calculations
sqrt = math.sqrt(num)
log = math.log(num)
sine = math.sin(num)

# Display results
print(f"Square root of {num} is: {sqrt}")
print(f"Natural log (ln) of {num} is: {log}")
print(f"Sine of {num} radians is: {sine}")
